package image;

public class MasterImage {

	/* attributes: */
	private String img;

	/* constructor: */
	public MasterImage() {}

	public MasterImage(String img) {
		this.img = img;
	}

	/* getter and setter: */

	public String getImg() { return this.img; }

	public void setImg(String img) { this.img = img; }
}
